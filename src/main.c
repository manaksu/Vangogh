// Precompute X start positions for 45 columns across 144px
// 9 cells of 4px + 36 cells of 3px = 144px
// Distribute the wider cells evenly: every 5th cell (0-indexed: 4,9,14,...,44)
static int s_col_x[46];  // s_col_x[c] = start x of column c; s_col_x[45] = 144
static int s_row_y[46];  // s_row_y[r] = start y of row r; s_row_y[45] = 168

static void build_cell_map(void) {
  // Columns: 9 cells of 4px + 36 of 3px across 144px
  // Place 4px cells at indices: 4, 9, 14, 19, 24, 29, 34, 39, 44
  int x = 0;
  for (int c = 0; c < 45; c++) {
    s_col_x[c] = x;
    // Every 5th cell (0-indexed: 4,9,14,...) gets 4px
    int w = ((c % 5) == 4) ? 4 : 3;
    x += w;
  }
  s_col_x[45] = 144;

  // Rows: 33 cells of 4px + 12 cells of 3px across 168px
  // Place 3px cells at every ~3rd row: indices 2,5,8,11,14,17,20,23,26,29,32,35
  // 33*4 + 12*3 = 132+36 = 168 ✓
  int y = 0;
  for (int r = 0; r < 45; r++) {
    s_row_y[r] = y;
    // 12 cells of 3px at approx every 3.75 rows → indices where (r*12/45) changes
    // Simple: cells at r=2,5,9,12,16,19,23,26,30,33,37,40 get 3px, rest 4px
    int three_px_rows[] = {2,5,9,12,16,19,23,26,30,33,37,40};
    int is3 = 0;
    for (int i = 0; i < 12; i++) {
      if (three_px_rows[i] == r) { is3 = 1; break; }
    }
    y += is3 ? 3 : 4;
  }
  s_row_y[45] = 168;
}

static GColor color_from_palette(uint8_t idx) {
  return GColorFromRGB(PALETTE_R[idx], PALETTE_G[idx], PALETTE_B[idx]);
}

static GColor gray_level_to_color(uint8_t level) {
  switch (level) {
    case 0: return GColorBlack;
    case 1: return GColorDarkGray;
    case 2: return GColorLightGray;
    default: return GColorWhite;
  }
}

static void mosaic_layer_update(Layer *layer, GContext *ctx) {
  for (int r = 0; r < GRID_H; r++) {
    for (int c = 0; c < GRID_W; c++) {
      GColor cell_color;
      if (s_display_mode == 0) {
        cell_color = color_from_palette(MOSAIC_COLOR[r][c]);
      } else {
        cell_color = gray_level_to_color(MOSAIC_GRAY[r][c]);
      }
      graphics_context_set_fill_color(ctx, cell_color);
      GRect cell = GRect(s_col_x[c], s_row_y[r],
                         s_col_x[c+1] - s_col_x[c],
                         s_row_y[r+1] - s_row_y[r]);
      graphics_fill_rect(ctx, cell, 0, GCornerNone);
    }
  }
}

static void update_time(struct tm *tick_time) {
  static char s_time_buf[6];
  strftime(s_time_buf, sizeof(s_time_buf),
           clock_is_24h_style() ? "%H:%M" : "%I:%M", tick_time);
  text_layer_set_text(s_time_layer, s_time_buf);
}

static void tick_handler(struct tm *tick_time, TimeUnits units_changed) {
  update_time(tick_time);
}

// ── Settings ──────────────────────────────────────────────────────────────────
static void load_settings(void) {
  s_display_mode = persist_exists(KEY_DISPLAY_MODE) ?
                   (uint8_t)persist_read_int(KEY_DISPLAY_MODE) : 0;
  uint32_t raw = persist_exists(KEY_TIME_COLOR) ?
                 (uint32_t)persist_read_int(KEY_TIME_COLOR) : 0xFFFFFFFF;
  s_time_color = (GColor){ .argb = (uint8_t)(raw & 0xFF) };
  if (!persist_exists(KEY_TIME_COLOR)) {
    s_time_color = GColorWhite;
  }
}

static void inbox_received(DictionaryIterator *iter, void *context) {
  Tuple *t;
  t = dict_find(iter, KEY_DISPLAY_MODE);
  if (t) {
    s_display_mode = (uint8_t)t->value->int32;
    persist_write_int(KEY_DISPLAY_MODE, s_display_mode);
  }
  t = dict_find(iter, KEY_TIME_COLOR);
  if (t) {
    s_time_color.argb = (uint8_t)t->value->int32;
    persist_write_int(KEY_TIME_COLOR, (int)s_time_color.argb);
  }
  text_layer_set_text_color(s_time_layer, s_time_color);
  layer_mark_dirty(s_mosaic_layer);
}

// ── Window ────────────────────────────────────────────────────────────────────
static void window_load(Window *window) {
  Layer *root = window_get_root_layer(window);
  GRect bounds = layer_get_bounds(root);

  build_cell_map();

  s_mosaic_layer = layer_create(bounds);
  layer_set_update_proc(s_mosaic_layer, mosaic_layer_update);
  layer_add_child(root, s_mosaic_layer);

  // Time label centred at bottom ~30px strip
  s_time_layer = text_layer_create(GRect(0, 140, 144, 28));
  text_layer_set_background_color(s_time_layer, GColorClear);
  text_layer_set_text_color(s_time_layer, s_time_color);
  text_layer_set_text_alignment(s_time_layer, GTextAlignmentCenter);
  text_layer_set_font(s_time_layer, fonts_get_system_font(FONT_KEY_LECO_26_BOLD_NUMBERS_AM_PM));
  layer_add_child(root, s_time_layer);

  time_t now = time(NULL);
  update_time(localtime(&now));
}

static void window_unload(Window *window) {
  layer_destroy(s_mosaic_layer);
  text_layer_destroy(s_time_layer);
}

static void init(void) {
  load_settings();
  app_message_open(64, 64);
  app_message_register_inbox_received(inbox_received);

  s_window = window_create();
  window_set_window_handlers(s_window, (WindowHandlers){
    .load   = window_load,
    .unload = window_unload,
  });
  window_stack_push(s_window, true);
  tick_timer_service_subscribe(MINUTE_UNITS, tick_handler);
}

static void deinit(void) {
  tick_timer_service_unsubscribe();
  window_destroy(s_window);
}

int main(void) {
  init();
  app_event_loop();
  deinit();
}
